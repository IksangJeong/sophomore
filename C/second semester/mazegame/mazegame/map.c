/* 맵의 특정 위치가 벽인지 확인하는 함수 */
int is_wall(int x, int y)
{
    /* 맵 범위 체크 */
    if (x < 0 || x >= MAP_WIDTH || y < 0 || y >= MAP_HEIGHT)
        return 1;  /* 맵 밖은 벽으로 처리 */

    return g_world_map[x][y] != 0;
}

/* 맵의 특정 위치의 벽 타입을 반환하는 함수 */
int get_wall_type(int x, int y)
{
    /* 맵 범위 체크 */
    if (x < 0 || x >= MAP_WIDTH || y < 0 || y >= MAP_HEIGHT)
        return 1;  /* 기본 벽 타입 반환 */

    return g_world_map[x][y];
}